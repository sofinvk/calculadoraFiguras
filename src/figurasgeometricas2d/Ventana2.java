    private void ingresarValorCuadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarValorCuadActionPerformed
         try{
            cuadrado.setBase(Double.valueOf(campoBaseCuad.getText()));
            cuadrado.setAltura(Double.valueOf(campoAlturaCuad.getText()));
            campoResultado.setText("Base: " + cuadrado.getBase()+ "\n"+
                    "Altura: " + cuadrado.getAltura());
        }catch(java.lang.NumberFormatException error){
            campoBaseCuad.setBackground(Color.yellow);
            campoBaseCuad.setText("Ingrese un valor");
            campoAlturaCuad.setBackground(Color.yellow);
            campoAlturaCuad.setText("Ingrese un valor");
        }
    }//GEN-LAST:event_ingresarValorCuadActionPerformed

