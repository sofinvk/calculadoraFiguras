    private void ingresarValorTriangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarValorTriangActionPerformed
         try{
            triangulo.setCatMay(Double.valueOf(campoCatMay.getText())); 
            triangulo.setCatMen(Double.valueOf(campoCatMen.getText()));
            triangulo.setHipotenusa(Double.valueOf(campoHipot.getText()));
            campoResultado.setText("Cateto Mayor: " + triangulo.getCatMay()+ "\n"+
                    "Cateto Menor: " + triangulo.getCatMen()+ "\n"+
                    "Hipotenusa: " + triangulo.getHipotenusa()
            );
            
        }catch(java.lang.NumberFormatException error){
            campoCatMay.setBackground(Color.yellow);
            campoCatMay.setText("Ingrese un valor");
            campoCatMen.setBackground(Color.yellow);
            campoCatMen.setText("Ingrese un valor");
            campoHipot.setBackground(Color.yellow);
            campoHipot.setText("Ingrese un valor");
        }
        
    }//GEN-LAST:event_ingresarValorTriangActionPerformed

